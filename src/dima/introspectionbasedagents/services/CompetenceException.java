package dima.introspectionbasedagents.services;

public class CompetenceException extends Exception {

	/**
	 *
	 */
	private static final long serialVersionUID = 8078977343518081350L;

	public CompetenceException() {
		super();
	}

	public CompetenceException(final String message) {
		super(message);
	}

	public CompetenceException(final Throwable cause) {
		super(cause);
	}

	public CompetenceException(final String message, final Throwable cause) {
		super(message, cause);
	}

}